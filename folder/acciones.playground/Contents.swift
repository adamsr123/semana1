import UIKit

for i in 0...100
{
    if i % 2 == 0
    {
        print("\(i) par")
    }
    else if i % 2 != 0
    {
        print("\(i) inpar")
    }
    
    if i < 40 && i > 30
    {
        print("\(i) viva swift!")
    }
    if i % 5 == 0
    {
        print("\(i) bingo")
    }
}
